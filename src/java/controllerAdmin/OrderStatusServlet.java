package controllerAdmin;

import data.dao.Database;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
@WebServlet(name = "OrderStatusServlet", urlPatterns = {"/order-status"})
public class OrderStatusServlet extends HttpServlet {
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>Servlet OrderStatusServlet</title>");
            out.println("</head>");
            out.println("<body>");
            out.println("<h1>Servlet OrderStatusServlet at " + request.getContextPath() + "</h1>");
            out.println("</body>");
            out.println("</html>");
        }
    }
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String idStr = request.getParameter("id");
        String newStatus = request.getParameter("status");
        
        if (idStr == null || newStatus == null) {
            response.sendRedirect("order-list");
            return;
        }
        try {
            int orderId = Integer.parseInt(idStr);
            boolean success = false;

            if ("Cancelled".equalsIgnoreCase(newStatus)) {
                success = Database.getOrderDao().cancelOrder(orderId);
            } else {
                success = Database.getOrderDao().updateStatus(orderId, newStatus);
            }

            if (!success) {
                System.out.println("Update status failed for Order ID: " + orderId);
            }

        } catch (NumberFormatException e) {
            e.printStackTrace();
        }

        response.sendRedirect("order-list");

    }
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }
    @Override
    public String getServletInfo() {
        return "Short description";
    }

}
